export type TranslationKey = keyof typeof translations.ko;

function getNested(obj: Record<string, unknown>, path: string): string | undefined {
  const parts = path.split('.');
  let cur: unknown = obj;
  for (const p of parts) {
    if (cur == null || typeof cur !== 'object') return undefined;
    cur = (cur as Record<string, unknown>)[p];
  }
  return typeof cur === 'string' ? cur : undefined;
}

export function translate(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>
): string {
  const koPack = translations.ko as Record<string, unknown>;
  const enPack = translations.en as Record<string, unknown>;
  const primary = locale === 'ko' ? koPack : enPack;

  const template =
    getNested(primary, key) ?? getNested(enPack, key) ?? getNested(koPack, key) ?? key;
  if (!params) return template;
  return Object.entries(params).reduce(
    (s, [k, v]) => s.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v)),
    template
  );
}
